    /// If the second tuple argument is Some(target), then it's another type of ID
    /// (which type can be figured out by matching the most significant nibble of the MSB,
    /// but that's not this method's job).
    pub fn source_target(&self) -> (Source, Option<Target>) {
        match self {
            ID(msb @ 0x00..=0x0F, lsb) => (self.to_mute(), None),
            ID(msb, lsb) => {
                let (source, target) = ID(msb & 0x0F, *lsb).to_source_target();
                (source, Some(target))
            }
        }
    }

    // This is an internal helper function, where the ID is guaranteed to be a mute.
    fn to_mute(&self) -> Source {
        use Source::*;
        match self {
            ID(0x00, lsb @ 0x00..=0x2F) => Input(lsb + 1),
            ID(0x00, lsb @ 0x30..=0x3B) => Group(lsb - 0x2F),
            ID(0x00, lsb @ 0x3C..=0x43) => FXRet(lsb - 0x3B),
            ID(0x00, 0x44) => LR,
            ID(0x00, lsb @ 0x45..=0x50) => Aux(lsb - 0x44),
            ID(0x00, lsb @ 0x51..=0x54) => FXSend(lsb - 0x50),
            ID(0x00, lsb @ 0x55..=0x57) => Mtx(lsb - 0x54),

            // @Todo: DCA
            // ID(0x02, lsb @ 0x00..=0x07) => Dca(lsb + 1),

            // @Todo: Mute groups
            // ID(0x04, lsb @ 0x00..=0x07) => MuteGroup(lsb + 1),
            _ => unimplemented!("invalid mute ID: {:?}", self),
        }
    }

